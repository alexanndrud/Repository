package com.project.Project.Formular.model;

public class FormData {
    private String platforma;
    private String nume;
    private String email;
    private String telefon;
    private String oras;
    private String observatii;

    // Getters & Setters
    public String getPlatforma() { return platforma; }
    public void setPlatforma(String platforma) { this.platforma = platforma; }

    public String getNume() { return nume; }
    public void setNume(String nume) { this.nume = nume; }

    public String getEmail() { return email; }
    public void setEmail(String email) { this.email = email; }

    public String getTelefon() { return telefon; }
    public void setTelefon(String telefon) { this.telefon = telefon; }

    public String getOras() { return oras; }
    public void setOras(String oras) { this.oras = oras; }

    public String getObservatii() { return observatii; }
    public void setObservatii(String observatii) { this.observatii = observatii; }
}

